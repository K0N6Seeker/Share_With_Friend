    // ‡πÄ‡∏ä‡πà‡∏ô 'note:1' ‡∏à‡∏∞‡πÑ‡∏î‡πâ ID ‡πÄ‡∏õ‡πá‡∏ô '1'
    const id = key.split(':')[1];

    // ‡πÅ‡∏™‡∏î‡∏á‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î‡∏Ç‡∏≠‡∏á‡πÇ‡∏ô‡πâ‡∏ï‡πÉ‡∏ô‡∏Ñ‡∏≠‡∏ô‡πÇ‡∏ã‡∏•‡∏î‡πâ‡∏ß‡∏¢‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡∏ó‡∏µ‡πà‡∏à‡∏±‡∏î‡∏£‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ö
    // ‡∏£‡∏ß‡∏°‡∏ñ‡∏∂‡∏á ID, ‡∏´‡∏±‡∏ß‡∏Ç‡πâ‡∏≠, ‡πÅ‡∏•‡∏∞‡πÄ‡∏ô‡∏∑‡πâ‡∏≠‡∏´‡∏≤ ‡∏û‡∏£‡πâ‡∏≠‡∏°‡∏≠‡∏¥‡πÇ‡∏°‡∏à‡∏¥‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ä‡∏±‡∏î‡πÄ‡∏à‡∏ô
    console.log(`üìù Note ID: ${id}`);
